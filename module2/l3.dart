class Mark
{
  var marks;
  void grades()
  {
    if (marks >= 91) {
      print("aa");
    }
    else if(marks>=81)
      {
        print("ab");
      }
    else if(marks>=71)
      {
        print("bb");
      }
    else if(marks>=61)
      {
        print("bc");
      }
    else if(marks>=51)
      {
        print("cc");
      }
    else if(marks>=41)
      {
        print("cd");
      }
    else if(marks>=40)
      {
        print("dd");
      }
    else{
      print("fail");
    }
  }
}