class Switch()
{
  void s1()
  {
    var num=3;

    Switch(num)
    {
      case 1:
        print("hello");
        break;

      case 2:
        print("how are you?");
        break;

      case 3:
        print("who are you?");
        break;

      default:
        print("number is not valid");
        break;
    }
  }
}