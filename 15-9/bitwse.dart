class Bitwise()
{
  void b1()
  {
    int a=5;
    int b=8;

    var c = a & b;
    print(c);

    var d = a | b;
    print(d);

    //Bitwise XOR
    var e = a ^ b;
    print(e);

    //Bitwise NOT
    var f = ~a;
    print(f);

    var g = a << b;
    print(g);

    var h = a >> b;
    print(h);

  }
}

